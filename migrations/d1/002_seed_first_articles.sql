<p>Replace cutting heads every 12-18 months for optimal performance. You''ll notice when it''s time ‚Äì the shave becomes less close and takes longer.</p>

<h3>Can I use shaving cream with electric shavers?</h3>

<p>Yes, if your shaver is rated for wet use. Shaving gel works better than foam. Many guys prefer dry shaving once they adjust to electric.</p>

<h3>Why does my shaver pull my hair?</h3>

